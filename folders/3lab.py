def Print(_i, _j): 
    # Виводимо значення _i та _j, додаючи "+" між ними
    print(_i, "+", _j) 
     
nums = [1, 3, 3, 6, 9, 4] 
 
for i in nums: 
    for j in nums: 
        # Перевіряємо, чи сума _i та _j дорівнює 10
        if i + j == 10: 
            # Виводимо значення _i та _j, які задовольняють умову
            print(i, j)
